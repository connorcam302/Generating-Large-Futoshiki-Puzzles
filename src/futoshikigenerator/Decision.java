package futoshikigenerator;

public class Decision {
	private int row;
	private int column;
	private int value;
	
	public void setRow(int val) {
		this.row = val;
	}
	
	public int getRow() {
		return this.row;
	}
	
	public void setCol(int val) {
		this.column = val;
	}
	
	public int getCol() {
		return this.column;
	}
	
	public void setValue(int val) {
		this.value = val;
	}
	
	public int getValue() {
		return this.value;
	}
}
